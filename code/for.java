for ( Laufvariable; Bedingung; Veränderung ) {
    // Bedingung muss True sein, damit die Anweisung ausgeführt wird.
    Anweisung;
}

// Example:
for (int counter = 1; counter <= 5; counter++) {
    System.out.println("Zaeler ist bei: " + counter);
}
// Ausgabe:
// Zaeler ist bei: 1
// Zaeler ist bei: 2
// Zaeler ist bei: 3
// Zaeler ist bei: 4
// Zaeler ist bei: 5