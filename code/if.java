int zahl = 2;
if (zahl <= 1){
    // Bedingung muss True sein, damit die Anweisung ausgeführt wird.
    System.out.println(zahl + " kleiner oder gleich 1");
} else if (zahl == 2) {
    System.out.println(zahl + " ist gleich 2");
} else {
    System.out.println(zahl + " ist größer als 2");
}
// Ausgabe:
// 2 ist gleich 2